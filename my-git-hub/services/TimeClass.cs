        System.Threading.Thread.Sleep(200);
        Console.Clear();
        Console.WriteLine(message + "...\\\\");
        System.Threading.Thread.Sleep(200);
        Console.Clear();
        Console.WriteLine(message + "...||");
        System.Threading.Thread.Sleep(200);
        Console.Clear();
        Console.WriteLine(message + "...//");
        System.Threading.Thread.Sleep(200);
        Console.Clear();
        Console.WriteLine(message + "...==");
        System.Threading.Thread.Sleep(200);
        Console.Clear();
        Console.WriteLine(message + "...\\\\");
        System.Threading.Thread.Sleep(200);
        Console.Clear();
        Console.WriteLine(message + "...||");
        System.Threading.Thread.Sleep(200);
        Console.Clear();
    }
}